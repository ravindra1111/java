package Arrays;

public class DataTyesUsingArrays {

	public static void main(String[] args) {
		byte  b[]= {14, 25, 36, 90};
		for(int i=0;i<b.length;i++) {
			System.out.println(b[i]);
		}
		{
			short c[]= {10,20,45,70};
			for(int j=0;j<c.length;j++) {
				System.out.println(c[j]);
			}
		}
		{
		  int d[]= {20,50,60,70};
		  for(int k=0;k<d.length;k++) {
			  System.out.println(d[k]);
		  }
		}
		{
			long e[]= {400,700,900,1000,5000,3000};
			for(int l=0;l<e.length;l++) {
				System.out.println(e[l]);
			}
		}
		{
			float g[]= {2.22f,4.22f,5.33f,8.22f};
			for(int m=0;m<g.length;m++) {
				System.out.println(g[m]);
			}
			
		}
		{
			double h[]= {2.499f,3.555f,6.999f,7.888f};
			for(int n=0;n<h.length;n++) {
				System.out.println(h[n]);
			}
			
		}
		{
			boolean t[]= {true,false};
			for(int z=0;z<t.length;z++) {
				System.out.println(t[z]);
			}
		}
		{
			char y[]= new char[1];
			for(int q=0;q<y.length;q++) {
				System.out.println(y[q]);
			}
		}
		{
			String a[]= {"Ravi,Raju,Mana,Sonu,Ramu"};
			for(int p=0;p<a.length;p++) {
				System.out.println(a[p]);
			}
			}
		}
		

	}


