package Operators;

public class Assing {

        int a=45;
        int b=50;
int ravi()
{
a+=b;
return a;
} 
int ravione()
{
a-=b;
return a;
}          
int ravitwo()
{
a*=b;
return a;
}  
int ravithree()
{
a/=b;
return a;
}    
int ravifour()
{
a%=b;
return a;
} 
}                

